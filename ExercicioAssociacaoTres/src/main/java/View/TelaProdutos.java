    private void jBPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPesquisarActionPerformed
        // TODO add your handling code here:
        try {
            String num = jTPesquisaNumero.getText();
            String nome = jTPesquisaNome.getText();

            Produto p = criarObjeto(nome, num);

            Produto prodCadastrado = produtosCadastrados.verificarProdutoCadastrado(p);

            if (prodCadastrado == null) {
                JOptionPane.showMessageDialog(this, "O produto não foi localizado.");
            } else {
                JOptionPane.showMessageDialog(this, "Produto Localizado!\n" + prodCadastrado.getNomeProduto() + " - R$" + prodCadastrado.getValor() + " - " + prodCadastrado.getNumProduto());
            }

            jTValorUnitario.setText("");
            jTNomeProduto.setText("");

        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            jTValorUnitario.setText("");
            jTNomeProduto.setText("");
        }
    }//GEN-LAST:event_jBPesquisarActionPerformed

    private void jBCadastrarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarProdutoActionPerformed
        // TODO add your handling code here:
        try {
            String textoValor = jTValorUnitario.getText();
            String nome = jTNomeProduto.getText();

            if (textoValor.isBlank()) {
                throw new IllegalArgumentException("Erro: O campo de valor unitário não pode estar vazio.");
            }

            double valor;
            try {
                valor = Double.parseDouble(textoValor.replace(",", "."));
            } catch (NumberFormatException e) {
                throw new IllegalArgumentException("Erro: O valor deve ser um número válido (ex: 10.50).");
            }

            Produto p = new Produto(nome, valor);
            produtosCadastrados.adicionarProduto(p);
            jTValorUnitario.setText("");
            jTNomeProduto.setText("");
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso.");
        } catch (NumberFormatException e) {
            throw new IllegalArgumentException("Erro: O valor deve ser um número válido (ex: 10.50).");
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jBCadastrarProdutoActionPerformed

