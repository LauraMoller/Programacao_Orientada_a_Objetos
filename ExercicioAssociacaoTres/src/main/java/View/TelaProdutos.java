    public Produto criarObjeto(String nome, String numero) {

        try {
            if (numero.isBlank() && nome.isBlank()) {
                JOptionPane.showMessageDialog(this, "É preciso informar ao menos o nome ou o número do produto.");
                return null;
            }

            int num = 0;
            if (!numero.isBlank()) {
                num = Integer.parseInt(numero);
            }

            if (nome.isBlank()) {
                return new Produto(num);
            }

            return new Produto(nome, num);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erro: formato númerico.");
        }
        return null;
    }

