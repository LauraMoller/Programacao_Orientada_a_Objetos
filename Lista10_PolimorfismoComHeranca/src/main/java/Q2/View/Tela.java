    private void jBCadastrarLinguagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarLinguagemActionPerformed
        // TODO add your handling code here:
        try {
            String nome = jTNomeFuncionarioOpProgramadores.getText();
            Funcionario f = supreme.procurarFuncionario(nome);

            if (f instanceof Programador) {
                Programador p = (Programador) f; // Cast seguro
                p.incluirLinguagem(JOptionPane.showInputDialog(this, "Linguagem que deseja adicionar"));
                JOptionPane.showMessageDialog(this, "Linguagem adicionada!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro: Este funcionário não é um Programador.");
            }
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        jTNomeFuncionarioOpProgramadores.setText("");

    }//GEN-LAST:event_jBCadastrarLinguagemActionPerformed

