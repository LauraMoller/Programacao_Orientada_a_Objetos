    private void jBAdicionarVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAdicionarVendaActionPerformed
        // TODO add your handling code here:
        try {
            String nome = jTNomeFuncionarioOpVendedores.getText();
            Funcionario f = supreme.procurarFuncionario(nome);

            if (f instanceof Vendedor) {
                Vendedor v = (Vendedor) f;
                double valorVenda = Double.parseDouble(JOptionPane.showInputDialog(this, "Valor da venda: "));
                v.incluirVenda(new Venda(valorVenda));
                JOptionPane.showMessageDialog(this, "Venda adicionada!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro: Este funcionário não é um Vendedor.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erro: formato numérico.");
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        jTNomeFuncionarioOpVendedores.setText("");
    }//GEN-LAST:event_jBAdicionarVendaActionPerformed

