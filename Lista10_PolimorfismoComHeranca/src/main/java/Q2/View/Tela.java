    private void jBAlterarPercentualComissaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAlterarPercentualComissaoActionPerformed
        // TODO add your handling code here:
        try {
            Vendedor v = (Vendedor) supreme.procurarFuncionario(jTNomeFuncionarioOpVendedores.getText());
            if (v == null) {
                JOptionPane.showMessageDialog(this, "Funcionário não encontrado!");
                return;
            }
            double percentual = Double.parseDouble(JOptionPane.showInputDialog(this, "Insira o novo percentual:"));
            v.setPercentualComissao(percentual);
            JOptionPane.showMessageDialog(this, "Percentual de Comissão Alterado.");
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        jTNomeFuncionarioOpVendedores.setText("");
    }//GEN-LAST:event_jBAlterarPercentualComissaoActionPerformed

