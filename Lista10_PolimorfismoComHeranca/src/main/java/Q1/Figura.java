package Q1;

/**
 * Classe abstrata que representa as características comuns das figuras
 * geométricas.
 *
 * @author Laura Möller
 */
public abstract class Figura {

    /**
     * Método abstrato que diz que toda figura geométrica possui um cálculo para
     * área.
     *
     * @return área.
     */
    public abstract double calcularArea();
}
