    private void jBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarActionPerformed
        // TODO add your handling code here:

        try {
            if (buttonGroup1.getSelection() == null || buttonGroup2.getSelection() == null) {
                JOptionPane.showMessageDialog(this, "Por favor, selecione o tipo de gasto e a forma de pagamento.");
                return;
            }

            TipoGasto tipoGasto = verificarTipoGasto();
            LocalDate data = LocalDate.parse(jTData.getText());
            if (data.isAfter(LocalDate.now())) {
                JOptionPane.showMessageDialog(this, "Erro: A data não pode ser maior que a data atual.");
                return;
            }
            double valor = Double.parseDouble(jTValor.getText());
            TipoPagamento tipoPagamento = verificarTipoPagamento();

            Gasto gasto = new Gasto(tipoGasto, data, valor, tipoPagamento);
            gerenciador.adicionarGasto(gasto);

            buttonGroup1.clearSelection();
            buttonGroup2.clearSelection();
            jTData.setText("");
            jTValor.setText("");

            JOptionPane.showMessageDialog(this, "Gasto cadastrado com suscesso!");

        } catch (java.time.format.DateTimeParseException e) {
            JOptionPane.showMessageDialog(this, "Erro: Data inválida ou fora do formato AAAA-MM-DD.");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erro: O valor informado é inválido.");
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jBCadastrarActionPerformed

    private void jBestatisticasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBestatisticasActionPerformed
        // TODO add your handling code here:
        try {
            if (!gerenciador.getTemGastosCadastrados()) {
                JOptionPane.showMessageDialog(this, "Não há gastos cadastrados para calcular!");
                return;
            } else {
                gerenciador.calcularGastos();
                gerenciador.calcularPorPagamento();

                jLRemedio.setText("Remédio: R$" + df.format(gerenciador.getGastosComRemedios()));
                jLRoupas.setText("Roupas: R$" + df.format(gerenciador.getGastosComRoupas()));
                jLRefeicoes.setText("Refeições: R$" + df.format(gerenciador.getGastosComRefeicoes()));
                jLOutros.setText("Outros: R$" + df.format(gerenciador.getGastosComRefeicoes()));

                jLCartao.setText("Cartão: R$" + df.format(gerenciador.getQuantoGastouCartao()));
                jLDinheiro.setText("Dinheiro: R$" + df.format(gerenciador.getQuantoGastouDinheiro()));
                jLCheque.setText("Cheque: R$" + df.format(gerenciador.getQuantoGastouCheque()));
                jLChequePre.setText("Cheque Pré: R$" + df.format(gerenciador.getQuantoGastouChequePre()));

                jLGastoMes.setText("Total gassto no mês: R$" + gerenciador.getGastoMes());
            }
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_jBestatisticasActionPerformed

    private TipoGasto verificarTipoGasto() {
        if (jRRemedio.isSelected()) {
            return TipoGasto.REMEDIO;
        } else if (jRRoupa.isSelected()) {
            return TipoGasto.ROUPA;
        } else if (jRRefeicao.isSelected()) {
            return TipoGasto.REFEICAO;
        } else if (jROutros.isSelected()) {
            return TipoGasto.OUTROS;
        } else {
            return null;
        }
    }

    private TipoPagamento verificarTipoPagamento() {
        if (jRDinheiro.isSelected()) {
            return TipoPagamento.DINHEIRO;
        } else if (jRCartao.isSelected()) {
            return TipoPagamento.CARTAO;
        } else if (jRCheque.isSelected()) {
            return TipoPagamento.CHEQUE;
        } else if (jRChequePre.isSelected()) {
            return TipoPagamento.CHEQUEPRE;
        } else {
            return null;
        }
    }

